    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    
        System.out.println("this address="+thisAddress+",port="+thisPort);
        try{

        // this is for sending the IP address to server when this client is ready...
        int newPort=3333;
        serverAddress = jTextField1.getText();
        remoteURL = "rmi://" + serverAddress + ":" + newPort + "/getallip";
        getip=(getallIPIntf)Naming.lookup(remoteURL);
        getip.storeIP(thisAddress);

	// create the registry and bind the name and object.
	registry = LocateRegistry.createRegistry( thisPort );
        ShutD = new ShutDownImpl();
        fileIntf = new FileImpl();
        fileTIntf = new FileTImpl();
        msgIntf = new MessageImpl();
        scintf = new ScreenCaptImpl();


        registry.rebind("shutdown", ShutD); 
        registry.rebind("fileDownload", fileIntf);
        registry.rebind("fileTransfer", fileTIntf);
        registry.rebind("message", msgIntf);
        registry.rebind("screen", scintf);


        jLabel3.setText("Status:     Connected to Server...");
        jButton1.setEnabled(false);
        jTextField1.setEnabled(false);
        jTextField2.setEnabled(false);

       }

        catch(RemoteException e){
            System.out.println("Remote Error: " + e);
            jLabel3.setText("Status:   Connection Refused try again...");
        }
        catch(Exception e)
        {
            System.out.println("Error: "+e);
            jLabel3.setText("Status:   Connection Refused try again...");
        }
               
    }//GEN-LAST:event_jButton1ActionPerformed

